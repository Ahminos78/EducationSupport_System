-- 验证学生 201～205 当前审核通过的课程。
SELECT
    student.id AS student_id,
    student.username,
    student.nickname,
    course.id AS course_id,
    course.code,
    course.name AS course_name,
    course.category,
    enrollment.status,
    enrollment.reviewed_at
FROM tb_enrollment enrollment
JOIN tb_user student ON student.id = enrollment.student_id
JOIN tb_course course ON course.id = enrollment.course_id
WHERE enrollment.student_id BETWEEN 201 AND 205
  AND enrollment.status = 1
ORDER BY enrollment.student_id, course.category, course.id;

-- 验证每名学生的已选课程数量。
SELECT
    enrollment.student_id,
    COUNT(*) AS approved_course_count
FROM tb_enrollment enrollment
WHERE enrollment.student_id BETWEEN 201 AND 205
  AND enrollment.status = 1
GROUP BY enrollment.student_id
ORDER BY enrollment.student_id;
